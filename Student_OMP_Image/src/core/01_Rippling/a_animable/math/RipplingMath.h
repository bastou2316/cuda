	    {
	    float dist;
	    dij(i,j,&dist);
	    *ptrlevelGris = 128+(127*((cos(dist)/10)-(t(7)))/((dist/10)+1));
	    }

	void dij(int i, int j, float* ptrResult)
	    {
	    *ptrResult = sqrt(pow(i-dim,2)+pow(j-dim,2));
	    }

